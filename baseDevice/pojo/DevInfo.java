package com.qhit.baseDevice.pojo;


public class DevInfo {
    private String companame;//公司名称
    private String devType; //设配类别
    private String conunt;  //数量或比例

    public String getCompaname() {
        return companame;
    }

    public void setCompaname(String companame) {
        this.companame = companame;
    }

    public String getDevType() {
        return devType;
    }

    public void setDevType(String devType) {
        this.devType = devType;
    }

    public String getConunt() {
        return conunt;
    }

    public void setConunt(String conunt) {
        this.conunt = conunt;
    }
}
